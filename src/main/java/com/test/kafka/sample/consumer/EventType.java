package com.test.kafka.sample.consumer;

public enum EventType {
    TELEPHONY_SUBSCRIPTION_EVENT("SA_TELEPHONY_SUBSCRIPTION"),
    TELEPHONY_CUSTOMER_ADDRESS_EVENT("SA_TELEPHONY_ADDRESS_UPDATE"),
    TELEPHONY_EQUIPMENT_EVENT("SA_TELEPHONY_SUBSCRIPTION"),
    TELEPHONY_RESOURCE_PLACEMENT_EVENT("SA_TELEPHONY_RESOURCE_PLACEMENT"),
    TELEPHONY_CHANGE_PHONE_NUMBER_EVENT("SA_TELEPHONY_SUBSCRIPTION"),
    TELEPHONY_REFRESH_EVENT(null),
    TELEPHONY_SUBSCRIPTION_EVENTs(null),
    TELEPHONY_SIGMA_STATE_CHANGE_EVENT("SA_TELEPHONY_SUBSCRIPTION");

    private final String topic;

    EventType(String topic) {
        this.topic = topic;
    }

    public String topic() {
        return topic;
    }
}
